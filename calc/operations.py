
"""Basic math operations."""

def add(a, b):
    """Add a and b."""
    a = int(a)
    b = int(b)
    return a + b

def sub(a, b):
    """Substract b from a."""
    a = int(a)
    b = int(b)
    return a - b

def mult(a, b):
    """Multiply a and b."""
    a = int(a)
    b = int(b)
    return a * b

def div(a, b):
    """Divide a by b."""
    a = int(a)
    b = int(b)
    return a / b


